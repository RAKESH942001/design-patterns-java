package org.example.behaviouralPattern.commandPattern;

public class Chef {

    public void cookPasta() {
        System.out.println("Chef is cooking pasta...");
    }

    public void cancelPasta() {
        System.out.println("Chef cancelled the pasta.");
    }

    public void cookSoup() {
        System.out.println("Chef is cooking soup...");
    }

    public void cancelSoup() {
        System.out.println("Chef cancelled the soup.");
    }


}
